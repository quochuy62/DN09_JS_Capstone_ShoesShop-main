//Class: Product
function Product(id, name, alias, price, description,size, shortDescription,quantity,deleted,categroies,relatedProducts,feature,image) {
    //Property = Value
    this.productID          = id;
    this.productName        = name;
    this.productAlias       = alias;
    this.price              = price;
    this.description        = description;
    this.size               = size;
    this.shortDescription   = shortDescription;
    this.quantity           = quantity;
    this.deleted            = deleted;
    this.categroies         = categroies;
    this.relatedProducts    = relatedProducts;
    this.feature            = feature;
    this.image              = image;
    
    //Method
}